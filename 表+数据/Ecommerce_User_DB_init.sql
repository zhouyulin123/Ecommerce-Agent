DROP DATABASE IF EXISTS Ecommerce_User_DB;
CREATE DATABASE Ecommerce_User_DB CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci;
USE Ecommerce_User_DB;

SET NAMES utf8mb4;

CREATE TABLE User_info (
    user_id INT PRIMARY KEY,   /*用户id*/
    user_name VARCHAR(50) NOT NULL, /*用户名*/
    phone CHAR(11) NOT NULL UNIQUE, /*手机号*/
    address VARCHAR(100) NOT NULL, /*地址*/
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP  /*创建时间*/
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE User_logs (
    log_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT NOT NULL, /*用户id*/
    user_name VARCHAR(50) NOT NULL, /*用户名*/
    shop_name VARCHAR(100) NOT NULL, /*店铺名称*/
    browse_item VARCHAR(100) NOT NULL, /*浏览商品*/
    enter_time DATETIME NOT NULL,   /*创建时间*/
    exit_time DATETIME NOT NULL,    /*结束时间*/
    FOREIGN KEY (user_id) REFERENCES User_info(user_id),   /*外键*/
    INDEX idx_logs_user_id (user_id), /*索引*/
    INDEX idx_logs_enter_time (enter_time) /*索引*/
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE User_Buy (
    buy_id INT PRIMARY KEY AUTO_INCREMENT, /*购买id*/
    user_id INT NOT NULL, /*用户id*/
    user_name VARCHAR(50) NOT NULL, /*用户名*/
    shop_name VARCHAR(100) NOT NULL, /*店铺名称*/
    buy_item VARCHAR(100) NOT NULL, /*购买商品*/
    enter_time DATETIME NOT NULL,   /*创建时间*/
    exit_time DATETIME NOT NULL, /*结束时间*/
    quantity INT NOT NULL DEFAULT 1, /*商品数量*/
    order_amount DECIMAL(10,2) NOT NULL, /*订单金额*/
    FOREIGN KEY (user_id) REFERENCES User_info(user_id),   /*外键*/
    INDEX idx_buy_user_id (user_id),
    INDEX idx_buy_enter_time (enter_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

